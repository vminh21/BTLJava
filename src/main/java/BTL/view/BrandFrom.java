    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
// Code cho nút THÊM
    if (txtNameBrand.getText().trim().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Chưa nhập tên thương hiệu!");
        return;
    }
    
    // Tạo đối tượng Brand (ID = 0)
    BTL.entity.Brands b = new BTL.entity.Brands(0, txtNameBrand.getText().trim(), txtOriginBrand.getText().trim());
    
    if (brandDao.insert(b) > 0) {
        javax.swing.JOptionPane.showMessageDialog(this, "Thêm thành công!");
        loadData(); // Load lại bảng ngay lập tức
        
        // Xóa trắng ô nhập
        txtNameBrand.setText(""); 
        txtOriginBrand.setText(""); 
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Thêm thất bại!");
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
// 1. Kiểm tra chọn dòng
    int row = tblBrandList.getSelectedRow();
    if (row == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng để xóa!");
        return;
    }
    
    // 2. Hỏi xác nhận (Tránh xóa nhầm)
    int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Bạn chắc chắn muốn xóa?", "Xác nhận", javax.swing.JOptionPane.YES_NO_OPTION);
    
    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
        // 3. Lấy ID và xóa
        int id = Integer.parseInt(tblBrandList.getValueAt(row, 0).toString());
        
        if (brandDao.delete(id)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Đã xóa thành công!");
            loadData(); // Load lại bảng
            
            // Xóa trắng form
            txtNameBrand.setText("");
            txtOriginBrand.setText("");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Xóa thất bại!");
        }
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblBrandListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBrandListMouseClicked
    int row = tblBrandList.getSelectedRow();
    if (row >= 0) {
        // Cột 1 là Tên, Cột 2 là Xuất xứ (Cột 0 là ID ẩn không cần hiện)
        txtNameBrand.setText(tblBrandList.getValueAt(row, 1).toString());
        
        Object origin = tblBrandList.getValueAt(row, 2);
        txtOriginBrand.setText(origin != null ? origin.toString() : "");
    }        // TODO add your handling code here:
    }//GEN-LAST:event_tblBrandListMouseClicked

