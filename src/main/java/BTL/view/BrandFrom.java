    // 3. HÀM ĐỔ DỮ LIỆU TỪ CSDL VÀO BẢNG
    private void loadData() {
        model.setRowCount(0);
        
        List<Brands> list = brandDao.getall(); 
        for (Brands b : list) {
         
            model.addRow(new Object[]{ b.getBrandId(), b.getName(), b.getOrigin() });
        }
    }
    // Hàm này sẽ được gọi khi bấm nút Tìm
     private void searchBrand() {
        String keyword = txtTimKiem.getText().trim().toLowerCase();
        model.setRowCount(0);
        List<Brands> list = brandDao.getall();
        for (Brands b : list) {
            if (b.getName().toLowerCase().contains(keyword) || 
                (b.getOrigin() != null && b.getOrigin().toLowerCase().contains(keyword))) {
                model.addRow(new Object[]{ b.getBrandId(), b.getName(), b.getOrigin() });
            }
        }
    }
