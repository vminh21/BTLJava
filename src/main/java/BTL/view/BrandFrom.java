    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
    // 1. Kiểm tra xem có chọn dòng nào chưa
    int row = tblBrandList.getSelectedRow();
    if (row == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần sửa!");
        return;
    }
    
    try {
        // 2. Lấy ID từ cột đầu tiên (Cột 0) của dòng đang chọn
        int id = Integer.parseInt(tblBrandList.getValueAt(row, 0).toString());
        
        // 3. Tạo đối tượng mới với thông tin đã sửa từ ô nhập
        BTL.entity.Brands b = new BTL.entity.Brands(id, txtNameBrand.getText().trim(), txtOriginBrand.getText().trim());
        
        // 4. Gọi hàm Update
        if (brandDao.update(b)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
            loadData(); // Load lại bảng
            
            // Xóa trắng form cho sạch
            txtNameBrand.setText("");
            txtOriginBrand.setText("");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Cập nhật thất bại!");
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnSuaActionPerformed

