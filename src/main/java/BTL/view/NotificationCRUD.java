    private void btnImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImageActionPerformed
        // TODO add your handling code here:
    JFileChooser fileChooser = new JFileChooser();
    javax.swing.filechooser.FileNameExtensionFilter filter = new javax.swing.filechooser.FileNameExtensionFilter("Hình ảnh", "jpg", "png", "gif");
    fileChooser.setFileFilter(filter);
    fileChooser.setAcceptAllFileFilterUsed(false);
    int result = fileChooser.showOpenDialog(this);
    if (result == JFileChooser.APPROVE_OPTION) {
        currentImagePath = fileChooser.getSelectedFile().getAbsolutePath();                
        updatePreviewImage(currentImagePath);        
        JOptionPane.showMessageDialog(this, "Đã chọn ảnh: " + fileChooser.getSelectedFile().getName());
    }
    }//GEN-LAST:event_btnImageActionPerformed
    
    private void updatePreviewImage(String path) {
    
    if (path == null || path.trim().isEmpty()) {
        lblPreview.setIcon(null);
        lblPreview.setText(""); 
        return;
    }

    try {
        File file = new File(path);
        
        if (!file.exists()) {
            lblPreview.setIcon(null);
            lblPreview.setText("");
            return;
        }

        //  Nếu file tồn tại, tiến hành Resize và hiển thị
        ImageIcon icon = new ImageIcon(path);
        java.awt.Image img = icon.getImage();
        
        int w = lblPreview.getWidth() > 0 ? lblPreview.getWidth() : 150;
        int h = lblPreview.getHeight() > 0 ? lblPreview.getHeight() : 150;
        
        java.awt.Image scaledImg = img.getScaledInstance(w, h, java.awt.Image.SCALE_SMOOTH);
        lblPreview.setIcon(new ImageIcon(scaledImg));
        lblPreview.setText(""); 

    } catch (Exception e) {
        lblPreview.setIcon(null);
        lblPreview.setText("");
    }
}
    
