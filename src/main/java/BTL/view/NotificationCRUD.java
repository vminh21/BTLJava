    private void initTable(){
            tableModel = new DefaultTableModel(
            new Object[]{"ID", "Tiêu đề", "Nội dung", "Đường dẫn ảnh"}, 0
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        jTable1.setModel(tableModel);

        // Ẩn cột ID để giao diện đẹp hơn
        jTable1.getColumnModel().getColumn(0).setMinWidth(0);
        jTable1.getColumnModel().getColumn(0).setMaxWidth(0);
    }      
    private void loadTable() {
        tableModel.setRowCount(0);
        try {
            List<Notification> list = dao.getall(); // Kiểm tra nếu DAO dùng getAll() thì sửa lại chữ A
            for (Notification n : list) {
                tableModel.addRow(new Object[]{
                    n.getNotificationId(),
                    n.getTitle(),
                    n.getMessage(),
                    n.getImage()
                });
            }
        } catch (Exception e) {
            System.out.println("Lỗi load bảng: " + e.getMessage());
        }
    }
    
    
    private void initTableEvent() {
        jTable1.getSelectionModel().addListSelectionListener(e -> {
            int row = jTable1.getSelectedRow();
            if (row >= 0) {
                selectedId = (int) jTable1.getValueAt(row, 0);
                txtTitle.setText(jTable1.getValueAt(row, 1).toString());
                txtMessage.setText(jTable1.getValueAt(row, 2).toString());
                // Lấy đường dẫn ảnh từ bảng gán vào biến tạm
                currentImagePath = jTable1.getValueAt(row, 3) != null ? jTable1.getValueAt(row, 3).toString() : "";
                updatePreviewImage(currentImagePath);
            }
        });
    }
    
    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        if (selectedId == -1) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn một dòng để sửa!");
        return;
    }

    Notification n = new Notification();
    n.setNotificationId(selectedId);
    n.setTitle(txtTitle.getText());
    n.setMessage(txtMessage.getText());
    n.setImage(currentImagePath);
    
    // PHẢI CÓ DÒNG NÀY ĐỂ TRUYỀN ID NGƯỜI DÙNG VÀO DATABASE
    if (currentUser != null) {
        n.setUserId(currentUser.getUserId()); 
    } else {
        // Nếu currentUser null, lấy đại ID = 1 (admin) để không lỗi DB khi test
        n.setUserId(1); 
    }

    dao.update(n);
    JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
    loadTable();
    resetForm();
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if (txtTitle.getText().isEmpty() || txtMessage.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập đủ tiêu đề và nội dung!");
        return;
    }

    Notification n = new Notification();
    n.setTitle(txtTitle.getText());
    n.setMessage(txtMessage.getText());
    n.setImage(currentImagePath);
    
    
    if (currentUser != null) {
        n.setUserId(currentUser.getUserId()); 
    }
    
    try {
        dao.insert(n);
        JOptionPane.showMessageDialog(this, "Thêm thành công!");
        loadTable();
        resetForm();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Lỗi khi thêm: " + e.getMessage());
    }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        if (selectedId == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một dòng để xóa!");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Xác nhận xóa?", "Xóa", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            dao.delete(selectedId);
            loadTable();
            resetForm();
        }
    }//GEN-LAST:event_btnXoaActionPerformed
    
    
