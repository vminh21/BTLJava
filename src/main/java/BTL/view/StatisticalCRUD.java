    private void updateRadioState() {
    Ngay.setEnabled(true);
    Thang.setEnabled(true);
    Nam.setEnabled(true);

    if (Ngay.isSelected()) {
        
        Thang.setEnabled(true);
        Nam.setEnabled(true);
        
        
        spnngay.setEnabled(true);
        spnthang.setEnabled(true);
        spnnam.setEnabled(true);
    } 
    else if (Thang.isSelected()) {
        
        Ngay.setEnabled(false);
        Nam.setEnabled(true);
        
        spnngay.setEnabled(false);
        spnthang.setEnabled(true);
        spnnam.setEnabled(true);
    } 
    else if (Nam.isSelected()) {
        
        Ngay.setEnabled(false);
        Thang.setEnabled(false);
        
        spnngay.setEnabled(false);
        spnthang.setEnabled(false);
        spnnam.setEnabled(true);
    }
}
