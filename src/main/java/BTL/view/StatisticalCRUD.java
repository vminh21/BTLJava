    private void btnTimkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimkiemActionPerformed
        // TODO add your handling code here:
    String type = Ngay.isSelected() ? "Ngay" : (Thang.isSelected() ? "Thang" : "Nam");
    
    int d = Integer.parseInt(spnngay.getSelectedItem().toString());
    int m = Integer.parseInt(spnthang.getSelectedItem().toString());
    int y = (int) spnnam.getValue();

    List<Orders> list = dao.getFiltered(type, d, m, y);
    if (list == null || list.isEmpty()) {
        tableModel.setRowCount(0); // Xóa bảng cho sạch
        lblTongHoaDonValue.setText("0");
        lblTongDoanhThuValue.setText("0 VNĐ");
        JOptionPane.showMessageDialog(this, "Không có hóa đơn nào trong thời gian này!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        return; // Dừng luôn không chạy code bên dưới nữa
    }
    tableModel.setRowCount(0); 
    
    double totalRevenue = 0; 
    int countDelivered = 0;  

    for (Orders o : list) {       
        tableModel.addRow(new Object[]{
            o.getOrderId(),
            o.getCreatedAt(),
            o.getStatus(), 
            String.format("%,.0f VNĐ", o.getTotalAmount())
        });     
        if (o.getStatus() != null && o.getStatus().equalsIgnoreCase("delivered")) {
            totalRevenue += o.getTotalAmount();
            countDelivered++;
        }
    }  
    lblTongHoaDonValue.setText(String.valueOf(list.size()));     
    lblTongDoanhThuValue.setText(String.format("%,.0f VNĐ", totalRevenue));
    
    
    }//GEN-LAST:event_btnTimkiemActionPerformed

